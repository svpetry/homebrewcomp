#include "std_utils.h"


/******************************************************************************/
char tolower(char c) {
	if (c >= 'A' && c <= 'Z')
		c = c - 'A' + 'a';
	return c;
}
/******************************************************************************/
word atoi(char const* str)
{
	char c;
	int result = 0;
	
	while (c = *str++)
		result = result * 10 + (c - '0');
	return result;
}
/******************************************************************************/
void puts(char *s) {
	char *p = s;
	while (*p) {
		putchar(*p);
		p++;
	}
	putchar('\n');
}
