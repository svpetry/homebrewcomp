#include <stdlib.h>
#include <string.h>

#include "utils_b0.h"

/******************************************************************************/
byte iswhite(char c) {
	return c == ' ' || c == '\t' || c == '\r';
}
/******************************************************************************/
byte isletter(char c) {
	return (c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z');
}
/******************************************************************************/
byte isnum(char c) {
	return (c >= '0' && c <= '9') || c == '.';
}
/******************************************************************************/

