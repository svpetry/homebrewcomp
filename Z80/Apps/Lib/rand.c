#include "rand.h"
#include "defs.h"
#include "io.h"

dword z1, z2, z3, z4;

/******************************************************************************/
void srand_dw() {
	word time;

	time = io_read(130) + ((int)io_read(131)) << 8;
	z1 = time;
	z2 = 12345;
	z3 = 12345;
	z4 = 12345;
}
/******************************************************************************/
dword rand_dw() {
   dword b;
   b  = ((z1 << 6) ^ z1) >> 13;
   z1 = ((z1 & 4294967294U) << 18) ^ b;
   b  = ((z2 << 2) ^ z2) >> 27; 
   z2 = ((z2 & 4294967288U) << 2) ^ b;
   b  = ((z3 << 13) ^ z3) >> 21;
   z3 = ((z3 & 4294967280U) << 7) ^ b;
   b  = ((z4 << 3) ^ z4) >> 12;
   z4 = ((z4 & 4294967168U) << 13) ^ b;
   return (z1 ^ z2 ^ z3 ^ z4);
}
